// ── Вспомогательная функция: получить описание по тесту и результату ──
export function getResultDescription(testId, result) {
  switch(testId) {
    case 'mbti':
      return MBTI_DESCRIPTIONS[result.type] || null;
    case 'eysenck':
      return EYSENCK_DESCRIPTIONS[result.temperament] || null;
    case 'pdo':
      return PDO_DESCRIPTIONS[result.leading] || null;
    case 'leonhard': {
      const top = Object.entries(result.scores || {}).sort((a,b)=>b[1]-a[1])[0];
      return top ? LEONHARD_DESCRIPTIONS[top[0]] : null;
    }
    case 'iq':
      return IQ_DESCRIPTIONS[result.category] || null;
    default:
      return null;
  }
}
