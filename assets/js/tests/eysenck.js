// ── Алгоритм подсчёта ─────────────────────────────────────────
function calculate(answers) {
  let E = 0, N = 0, L = 0;

  KEYS.E_yes.forEach(id => { if (answers[id] === 1) E++; });
  KEYS.N_yes.forEach(id => { if (answers[id] === 1) N++; });
  KEYS.L_yes.forEach(id => { if (answers[id] === 1) L++; });
  KEYS.L_no.forEach(id  => { if (answers[id] === 0) L++; });

  const valid = L <= 4;

  let temperament;
  if      (E >= 12 && N >= 12) temperament = 'Холерик';
  else if (E >= 12 && N <  12) temperament = 'Сангвиник';
  else if (E <  12 && N >= 12) temperament = 'Меланхолик';
  else                          temperament = 'Флегматик';

  return { E, N, L, valid, temperament };
}

