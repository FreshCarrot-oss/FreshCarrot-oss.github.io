// ── Алгоритм подсчёта ─────────────────────────────────────────
function calculate(answers) {
  const scores = {};

  Object.entries(KEYS).forEach(([scale, key]) => {
    let raw = 0;
    key.yes.forEach(id => { if (answers[id] === 1) raw++; });
    key.no.forEach(id  => { if (answers[id] === 0) raw++; });
    const mult = MULTIPLIERS[scale] || 1;
    scores[scale] = raw * mult;
  });

  // Определяем уровни: до 12 — норма, 13–18 — акцентуация, 19+ — выраженная
  const levels = {};
  Object.entries(scores).forEach(([scale, score]) => {
    if (score >= 19)      levels[scale] = 'strong';
    else if (score >= 13) levels[scale] = 'accent';
    else                  levels[scale] = 'norm';
  });

  return { scores, levels };
}

