// ── Весовая таблица: агрегируем веса всех наборов ────────────
// Алгоритм: выбор "+" добавляет веса, выбор "−" вычитает

function calculate(answers) {
  const scores = { Г:0, Ц:0, Л:0, А:0, С:0, П:0, Ш:0, Э:0, И:0, Н:0, К:0 };

  sets.forEach(set => {
    const answer = answers[set.id];
    if (!answer || typeof answer !== 'object') return;

    const { plus, minus } = answer;

    if (plus !== null && plus !== undefined) {
      const weights = set.answers[plus]?.weights || {};
      Object.entries(weights).forEach(([type, w]) => { scores[type] = (scores[type] || 0) + w; });
    }

    if (minus !== null && minus !== undefined) {
      const weights = set.answers[minus]?.weights || {};
      Object.entries(weights).forEach(([type, w]) => { scores[type] = (scores[type] || 0) - w; });
    }
  });

  // Определяем ведущий тип, второй и фоновые
  const sorted = Object.entries(scores).sort((a,b) => b[1] - a[1]);
  const TYPE_NAMES = {
    Г:'Гипертимный', Ц:'Циклоидный', Л:'Лабильный', А:'Астено-невротический',
    С:'Сенситивный', П:'Психастенический', Ш:'Шизоидный', Э:'Эпилептоидный',
    И:'Истероидный', Н:'Неустойчивый', К:'Конформный'
  };

  const leading   = TYPE_NAMES[sorted[0][0]];
  const secondary = sorted[1][1] > 0 ? TYPE_NAMES[sorted[1][0]] : null;
  const background = sorted.slice(2)
    .filter(([,v]) => v > 0)
    .slice(0,3)
    .map(([k]) => TYPE_NAMES[k]);

  const profileLetters = sorted.filter(([,v]) => v > 2).map(([k]) => k).join('');

  return {
    profile: profileLetters || sorted[0][0],
    leading,
    secondary,
    background,
    scores,
  };
}

